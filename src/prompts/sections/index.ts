// ─────────────────────────────────────────────────
// Section 1: Current Cultural Context
// ─────────────────────────────────────────────────

export const SECTION_1_CULTURAL_CONTEXT = `
SECTION GUIDANCE: CURRENT CULTURAL CONTEXT

Open by grounding the reader in what's happening in culture RIGHT NOW that makes this brand's message relevant. This is the "moving vehicle" — the existing cultural momentum the brand can authentically join.

Identify:
- Active conversations, debates, or tensions in culture that relate to this brand's territory
- Emerging behaviors, rituals, or shifts in consumer values
- Specific cultural data points (from the cultural intelligence provided) that create urgency
- Why THIS moment is uniquely right for this brand to act

Voice: Write as an observer of culture who sees patterns others miss. Be specific — cite real trends, real platforms, real behaviors. Avoid generic statements like "people are spending more time online."

Transition: This section should naturally lead into why THIS brand has a unique role to play in this cultural moment.
`;

// ─────────────────────────────────────────────────
// Section 2: Brand Credibility
// ─────────────────────────────────────────────────

export const SECTION_2_BRAND_CREDIBILITY = `
SECTION GUIDANCE: BRAND CREDIBILITY

Establish the brand's authentic "right to play" in the cultural space identified above. This is NOT about what the brand SAYS — it's about what the brand HAS DONE and IS.

Cover:
- Historical brand actions, heritage, or product truths that give permission
- The genuine intersection between the brand's DNA and the cultural moment
- Why consumers would accept this brand entering this space (vs. seeing it as opportunistic)
- Specific proof points that make the brand's entry believable

Voice: Confident but not arrogant. JL believes "humbleness is a virtue." Frame the brand's credibility as earned, not claimed. Reference real brand history where the retrieved context provides it.

Transition: This should build naturally toward the Shared Interest — the place where brand truth meets consumer passion.
`;

// ─────────────────────────────────────────────────
// Section 3: The Shared Interest
// ─────────────────────────────────────────────────

export const SECTION_3_SHARED_INTEREST = `
SECTION GUIDANCE: THE SHARED INTEREST

THIS IS THE MOST IMPORTANT SECTION. The Shared Interest is the strategic heart of every JL platform.

The Shared Interest is the reframe of the original brief into something that:
1. Allows the brand to RISE ABOVE its category
2. Creates participatory work because the consumer is PREDISPOSED and ALIGNED
3. BREAKS THROUGH advertising noise

This is NOT about the product. It is the intersection where what the brand genuinely cares about meets what the target audience is already passionate about. It should feel like a DISCOVERY, not a manufacture.

If the project seed includes a strategy deck or shared interest reframe from the JL strategy team, USE IT as the foundation and build upon it. If not, derive one from the brand's credibility and the cultural context.

Voice: This should feel like an "aha moment" — a realization that brand and consumer were always aligned on something deeper than a transaction. Write it as a revelation, not a positioning statement.

Transition: The Shared Interest should make the reader immediately see why a passive, broadcast approach would be a waste of this opportunity.
`;

// ─────────────────────────────────────────────────
// Section 4: The Passive Trap
// ─────────────────────────────────────────────────

export const SECTION_4_PASSIVE_TRAP = `
SECTION GUIDANCE: THE PASSIVE TRAP

Articulate the conventional, broadcast approach that a traditional agency would default to with this idea. Name it clearly so the team can avoid it.

Cover:
- What the "safe," non-participatory version of this campaign would look like
- Why that approach would underperform (waste the cultural moment, ignore consumer empowerment)
- The specific limitations of the passive approach for THIS brand and THIS moment
- Frame it as a "trap" the industry falls into — not a failure of imagination, but a default mode

Voice: Direct and slightly provocative. JL's philosophy is that "disrupting our way to attention" is over. Use this section to make the case for participation by showing what happens without it.

Transition: This should create natural tension that the next section resolves — the reader should be thinking "so what SHOULD we do instead?"
`;

// ─────────────────────────────────────────────────
// Section 5: The Participation Worthy Idea
// ─────────────────────────────────────────────────

export const SECTION_5_PARTICIPATION_IDEA = `
SECTION GUIDANCE: THE PARTICIPATION WORTHY IDEA

This is the creative transformation — the "Unfinished Platform" that requires the audience to complete it. This section should feel like the resolution of everything that came before.

The best participation ideas:
- Cannot exist without audience contribution (the idea is literally incomplete without them)
- Create something together that neither brand nor consumer could make alone
- Feel like an INVITATION, not an assignment
- Have clear value exchange — participation must feel rewarding, not extractive
- Are inherently shareable — people want others to see what they contributed

Voice: Bold and inspirational. This is where JL's creative ambition shines. Write it like a manifesto that makes people want to participate just from reading the description. Reference patterns from JL's past work (VW "Think Small," Adidas platform thinking, Google Rebrief) where relevant.

Transition: With the idea established, the reader should naturally wonder "where and when does this happen?"
`;

// ─────────────────────────────────────────────────
// Section 6: Moments and Places
// ─────────────────────────────────────────────────

export const SECTION_6_MOMENTS_PLACES = `
SECTION GUIDANCE: MOMENTS AND PLACES

Identify the specific environments, moments, and contexts where the target audience is MOST PRIMED to participate. This is about finding people at their most receptive.

Cover at least 3-5 of:
- Physical locations where the audience gathers
- Digital platforms and communities where they're most active
- Cultural moments (events, seasons, tentpoles) that create natural amplification
- Emotional states or life moments when the message resonates most
- Micro-moments in daily life that create organic entry points
- Contextual triggers that make participation feel natural, not forced

Voice: Specific and strategic. Don't just say "social media" — name the platform, the format, the behavior. Reference real cultural moments with dates where possible. Use the cultural intelligence data to ground recommendations in what's actually happening.

Transition: Once the reader knows WHERE to reach people, they'll want to know HOW — the specific mechanics.
`;

// ─────────────────────────────────────────────────
// Section 7: Mechanics of Participation
// ─────────────────────────────────────────────────

export const SECTION_7_MECHANICS = `
SECTION GUIDANCE: MECHANICS OF PARTICIPATION

Define 3-5 specific, actionable ways consumers can participate. These are the tactical "how" — concrete mechanisms that turn the idea into reality.

Each mechanic should include:
- What the consumer DOES (the action)
- Where it happens (platform/location)
- What they GET from participating (value exchange)
- How it spreads (built-in virality)

Types to consider:
- Digital tools or interactive experiences
- Physical challenges, installations, or activations
- Social media prompts, formats, or challenges
- User-generated content frameworks
- Real-world interactions that bridge to digital
- Gamification or competitive elements

Voice: Tactical and concrete. Each mechanic should be described clearly enough that a creative team could immediately start concepting executions. Reference similar mechanics from JL's past work where they exist in the retrieved context.

Transition: With the mechanics defined, the reader should be thinking about WHO will make this real — the first wave of participants.
`;

// ─────────────────────────────────────────────────
// Section 8: First Responders
// ─────────────────────────────────────────────────

export const SECTION_8_FIRST_RESPONDERS = `
SECTION GUIDANCE: FIRST RESPONDERS

Identify the specific niche subcultures that will ignite participation. These are NOT mass audiences — they are the spark that lights the fire.

First Responders must be:
- Authentically connected to the Shared Interest (not just paid influencers)
- Influential within their communities (not necessarily large followings)
- Likely to create and share WITHOUT incentive (intrinsic motivation)
- Small enough to feel SEEN by the brand, large enough to spark broader spread
- Credible enough that their participation signals authenticity to others

For each First Responder group, identify:
- Who they are (specific community, not just a demographic)
- Where they live online and offline
- Why they would care about this brand/idea
- What kind of content/participation they would naturally create
- How their participation would spread to adjacent communities

Voice: Demonstrate deep subculture fluency. Use the language of these communities. Show that JL understands these groups as people, not as "targets." Reference the cultural intelligence data for active communities.

Transition: With the first wave defined, paint the picture of what happens when this spreads beyond the initial community.
`;

// ─────────────────────────────────────────────────
// Section 9: The Ripple Effect
// ─────────────────────────────────────────────────

export const SECTION_9_RIPPLE_EFFECT = `
SECTION GUIDANCE: THE RIPPLE EFFECT

Close by painting the picture of the measurable impact — both cultural and business. This is not aspirational fluff; it's a confident projection of what participation creates.

Cover:
- Cultural legacy: How will this shift conversations? What new behaviors will it create?
- Business impact: Awareness, consideration, sales — specific to the brand's category
- Earned media potential: What makes this newsworthy? Why will journalists/creators cover it?
- Long-term brand equity: How does this participation become part of the brand's DNA?
- Compounding returns: How does each wave of participation fuel the next?

Voice: Visionary but grounded. End on a note of confident ambition. The reader should finish feeling that this idea MUST be made, that NOT doing it would be the real risk. Reference measurable outcomes from similar JL campaigns where available.

Final note: This section should leave the reader feeling momentum — a natural desire to move to execution.
`;

