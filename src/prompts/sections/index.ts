/**
 * All 9 section prompts in order
 */
export const FRAMEWORK_SECTIONS = [
  { number: 1, title: 'Current Cultural Context', prompt: SECTION_1_CULTURAL_CONTEXT },
  { number: 2, title: 'Brand Credibility', prompt: SECTION_2_BRAND_CREDIBILITY },
  { number: 3, title: 'The Shared Interest', prompt: SECTION_3_SHARED_INTEREST },
  { number: 4, title: 'The Passive Trap', prompt: SECTION_4_PASSIVE_TRAP },
  { number: 5, title: 'The Participation Worthy Idea', prompt: SECTION_5_PARTICIPATION_IDEA },
  { number: 6, title: 'Moments and Places', prompt: SECTION_6_MOMENTS_PLACES },
  { number: 7, title: 'Mechanics of Participation', prompt: SECTION_7_MECHANICS },
  { number: 8, title: 'First Responders', prompt: SECTION_8_FIRST_RESPONDERS },
  { number: 9, title: 'The Ripple Effect', prompt: SECTION_9_RIPPLE_EFFECT },
] as const;

/**
 * Get a specific section prompt by number
 */
export function getSectionPrompt(sectionNumber: number): string {
  const section = FRAMEWORK_SECTIONS.find(s => s.number === sectionNumber);
  if (!section) {
    throw new Error(`Invalid section number: ${sectionNumber}. Must be 1-9.`);
  }
  return section.prompt;
}

/**
 * Get all section prompts concatenated (for single-pass generation)
 */
export function getAllSectionPrompts(): string {
  return FRAMEWORK_SECTIONS.map(s => s.prompt).join('\n');
}
